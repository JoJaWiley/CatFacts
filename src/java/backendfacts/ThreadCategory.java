package backendfacts;

public class ThreadCategory {
    private int threadCatID;
    private String title;
    
    public int getThreadCatID() {
        return threadCatID;
    }
    
    public void setThreadCatID(int threadCatID) {
        this.threadCatID = threadCatID;
    }
    
    public String getTitle() {
        return title;
    }
    
    public void setTitle(String title) {
        this.title = title;
    }
}
